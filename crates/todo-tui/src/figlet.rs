/// Simple ASCII art text renderer (figlet-style)
/// Uses a minimal font for workspace names

/// Render text in ASCII art style
pub fn render(text: &str) -> Vec<String> {
    // Limit to reasonable length
    let text = if text.len() > 20 {
        &text[..20]
    } else {
        text
    };

    // Build each line of the ASCII art
    let mut lines = vec![String::new(); 5];

    for ch in text.chars() {
        let glyph = get_glyph(ch);
        for (i, glyph_line) in glyph.iter().enumerate() {
            lines[i].push_str(glyph_line);
        }
    }

    lines
}

